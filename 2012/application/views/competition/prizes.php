<h2>Judging Criteria</h2>
<p>Sites will be judged by four criteria:</p>

<ul>
	<li><b>Usability</b>: how well it is designed for users</li>
	<li><b>Functionality</b>: does it achieve its stated goals</li>
	<li><b>Originality and addressing the theme</b>: does the site do something interesting and new while fitting in with the theme</li>
	<li><b>Aesthetics</b>: how attractive is its look and feel</li>
</ul>

<p>More details about each category are below.</p>
<p>Judges will weight the four criteria roughly equally when assigning an overall score to a site.</p>

<h3>Usability</h3>
<p>Factors to consider in judging usability:</p>

<p><b>Ease of learning</b>: can a user encountering this web site for the first time easily discover what the web site does and how to make it work? Is its design consistent, both internally (among different web pages of the site) and externally (with other web sites)? Common failures of learnability include: buttons that aren't recognizable as buttons; navigation controls that appear in different places on different pages; menus that are hard to find; using technical jargon instead of common words.</p>

<p><b>Visibility</b>: is important information and system state readily visible on the site? Failures of visibility include: not showing when the user is logged in; not displaying the number of results of a search; omitting crucial fields from a search</p>

<p><b>Error handling</b>: is it hard to make errors and easy to recover from them? Failures of error handling include: not supporting Back or Undo; not allowing user to edit data they entered; inscrutable error messages.</p>

<p><b>Efficiency</b>: is the site efficient to operate? This generally does not refer to response time, but to speed of user input. Failures of efficiency include: tiny buttons that are hard to hit; use of cascading menus that create "steering tasks", which are much slower than pointing tasks.</p>

<h3>Functionality</h3>
<p>Factors to consider in judging functionality:</p>

<p><b>Broken links or buttons</b>: important features that are not implemented or that generate error messages.</p>
<p><b>Wrong answers</b>: if a search produces the wrong results, or sorting doesn't sort right, or posting a message to a database doesn't seem to store it.</p>
<p><b>Missing features</b>: functions that seem essential to the site that weren't included in the design at all. For example, if a comparison-shopping site doesn't offer any way to sort by price, that's a pretty serious deficiency in functionality.</p>

<h3>Originality and addressing the theme</h3>
<p>Factors to consider in judging originality and addressing the theme:</p>

<p><b>Novelty to 6.470</b>: is the idea of this site rare or unique among the 6.470 contestants? Or is it yet another site that does X?</p>
<p><b>Novelty in the Web</b>: is the idea of this site rare or unique on the Web at large?</p>
<p><b>Interest</b>: is it a cool idea? Would you or people you know want to use it? Is the problem it is addressing important?</p>
<p><b>Addressing the theme</b>: does your site address the theme well? You may have a novel, original idea but may have strayed to far to arrive at it. The themes should really be at the core of your website.</p>
	
<h3>Aesthetics</h3>
<p>Aesthetics largely concerns the graphic design of the site:</p>

<p><b>Simplicity</b>: does the site have a minimalist design that nevertheless packs a lot of information or functionality? Common failures of simplicity include dense, cluttered layouts with unnecessary information; unnecessary labels on data fields that would otherwise label themselves (e.g. "Date: January 30"); using too many colors, fonts, or alignment positions.</p>
<p><b>Color design</b>: does the site have an attractive color scheme, or does it clash? Does it use only a small number of colors, and exploit color to draw attention where needed?</p>
<p><b>Balance, symmetry, and whitespace</b>: are the pages unbalanced, heavy with ink on one side but not the other? Is the content lost in a sea of whitespace, or is there an appropriate relationship between figure and ground?</p>


