<p>We want to encourage all teams to use Git for their revision control system needs or at least use <i>a</i> revision control system for their code. There are an number of revision control systems out there, Git, CVS, SVN, Mercurial... You are welcome to use any of these by the staff will be able to provide the most support for Git.</p>

<h2><strong>Github</strong></h2>
<p>
If you do not want the overhead of setting up your own git repository, you can go to <a href="http://www.github.com" target="_blank">github.com </a> and it will set up a repository for you on their servers. Public repositories are free and if you would like a private one, please fill out this <a href="https://docs.google.com/spreadsheet/embeddedform?formkey=dEFyN0o0a0otM0FLWjFLQlJMTmVhQnc6MQ" target="_blank">form </a> and we will set you up with a private repository. </p>

