<h2>The Basics</h2>
<p>
	MIT 6.470 is a web programming class and competition that takes place over the IAP
	period at MIT.  We are student-run with generous support from professors, administrators,
	and external sponsors. This is our 5th year running the competition.  We've made huge 
	strides from the first time that 6.470 was run and look to make it even bigger and 
	better this year.
</p>

<h2>Mission Statement</h2>
<p>
	Our #1 goal is to teach students how to build a dynamic database-backed website from
	the ground up.  Building a website requires understanding and utilizing a variety of 
	technologies.  While we're certain everyone at MIT has what it takes to understand and
	navigate the space, doing so can be a long and tedious process.  We streamline the process,
	by teaching the core essentials and pointing out where advanced technologies fit into the
	mix.
</p>
<p>
	We also aim to make 6.470 accessible to as wide a variety of students as possible 
	(including those with limited or no programming experience).  There are many components
	of a website that don't require hardcore programming experience, e.g. graphic design,
	UI/UX, SEO, etc.  Non-programmers are often times just as good, if not better at these 
	areas.  Some of the better 6.470 teams have successfully matched talents in such ways
	to achieve success.  A very important skill is knowing where your time is best allocated.
	This is especially true in web programming.  It is one of our goals to show you the ropes
	of how to use the right resources and proper planning to maximize your efforts.
</p>

