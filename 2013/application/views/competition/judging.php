<h1 class="headline center"><strong>Judging Criteria</strong></h1>

<div class="hr"></div>

<p>To be considered for judging, your team and the site you produce must abide by the <b><?php echo HTML::anchor('competition/rules', 'competition rules'); ?> </b>. </p>

<p>When judging contest submissions, we will focus on three main areas:</p>

<ul>
	<li><b><a href="#usability">Usability</a></b>: the ease-of-use and learnability of the site</li>
	<li><b><a href="#presentation">Presentation</a></b>: the look-and-feel of the site </li>
	<li><b><a href="#viability">Viability</a></b>: the likelihood of your site to attract and retain a user base</li>
</ul>

<p>More details about each category are below.</p>

<a name="usability"></a>
<h3>Usability</h3>
<p>Usability concerns the ease-of-use and learnability of the site. Factors to consider in judging usability:</p>
<ul style="list-style-type:none">
<li><b>Ease of learning</b>: can a user encountering this web site for the first time easily discover what the web site does and how to make it work?</li>
<li><b>Visibility</b>: is important information and system state readily visible on the site?</li>
<li><b>Ease of use</b>: is the site easy and efficient for the user to operate? Efficiency generally does not refer to response time, but to how easily the user can perform common tasks on the site.</li>
<li><b>Functionality</b>: are there portions of the site that are not implemented or don't work as intended?</li>
</ul>

<a name="presentation"></a>
<h3>Presentation</h3>
<p>Presentation concerns the overall look-and-feel of the site and the impression it makes on the user. Factors to consider in judging presentation:</p>
<ul>
<li><b>Simplicity</b>:Are there unnecessary or redundant components in your site? Is every component of your site essential? Is your site cluttered?</li>
<li><b>Aesthetics</b>: does your site have a compelling visual design that is pleasing to use? Judges will not give extra points for amazing graphic or visual design. However, poor aesthetics may negatively impact the impression your site makes on the judges. Things that may impact aesthetics include:
<ul style="margin-bottom:0px">
  <li>layout</li>
  <li>color choice</li>
  <li>symmetry and balance</li>
  <li>whitespace</li>
  <li>typography</li>
  <li>visual consistency across the site</li>
</ul>
</li>
<li><b>Freshness</b>: Does your site do something particularly fresh, bold, or innovative? This could be in the execution of a feature or a visualization.</li>
</ul>

<a name="viability"></a>
<h3>Viability</h3>
<p>Viability concerns whether the judges believe your site has the ability to attract and retain a user base. Factors to consider in judging viability:</p>
<ul>
<li><b>Feature set</b>: Does your site have a targeted set of features that will appeal to your intended audience?  Are there essential features that are clearly missing?</li>
<li><b>Utility</b>: Does your site solve a pressing need for a particular demographic? </li>
<li><b>Originality</b>: Is your site the only one of its kind? What are its killer features? It is not essential that your site concept  is original, but if there are many other websites that do the same thing, your website should be particularly well-executed.</li>
<li><b>User re-engagement</b>: Will users want to come back to your site?</li>
</ul>
Note: we will not consider whether your site has potential to make money.
